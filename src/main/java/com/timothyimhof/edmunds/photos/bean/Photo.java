package com.timothyimhof.edmunds.photos.bean;

import java.util.List;

/**
 * The Class Photo.
 */
public class Photo {
	
	/** The id. */
	private String id;
	
	/** The dam. */
	private String dam;
	
	/** The author names. */
	private List<String> authorNames;
	
	/** The caption transcript. */
	private String captionTranscript;
	
	/** The type. */
	private String type;
	
	/** The sub type. */
	private String subType;
	
	/** The shot type abbreviation. */
	private String shotTypeAbbreviation;
	
	/** The photo srcs. */
	private List<String> photoSrcs;
	
	/** The source. */
	private String source;
	
	/** The vdp order. */
	private String vdpOrder;
	
	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(String id) {
		this.id = id;
	}
	
	/**
	 * Gets the dam.
	 *
	 * @return the dam
	 */
	public String getDam() {
		return dam;
	}
	
	/**
	 * Sets the dam.
	 *
	 * @param dam the new dam
	 */
	public void setDam(String dam) {
		this.dam = dam;
	}
	
	/**
	 * Gets the author names.
	 *
	 * @return the author names
	 */
	public List<String> getAuthorNames() {
		return authorNames;
	}
	
	/**
	 * Sets the author names.
	 *
	 * @param authorNames the new author names
	 */
	public void setAuthorNames(List<String> authorNames) {
		this.authorNames = authorNames;
	}
	
	/**
	 * Gets the caption transcript.
	 *
	 * @return the caption transcript
	 */
	public String getCaptionTranscript() {
		return captionTranscript;
	}
	
	/**
	 * Sets the caption transcript.
	 *
	 * @param captionTranscript the new caption transcript
	 */
	public void setCaptionTranscript(String captionTranscript) {
		this.captionTranscript = captionTranscript;
	}
	
	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	
	/**
	 * Sets the type.
	 *
	 * @param type the new type
	 */
	public void setType(String type) {
		this.type = type;
	}
	
	/**
	 * Gets the sub type.
	 *
	 * @return the sub type
	 */
	public String getSubType() {
		return subType;
	}
	
	/**
	 * Sets the sub type.
	 *
	 * @param subType the new sub type
	 */
	public void setSubType(String subType) {
		this.subType = subType;
	}
	
	/**
	 * Gets the shot type abbreviation.
	 *
	 * @return the shot type abbreviation
	 */
	public String getShotTypeAbbreviation() {
		return shotTypeAbbreviation;
	}
	
	/**
	 * Sets the shot type abbreviation.
	 *
	 * @param shotTypeAbbreviation the new shot type abbreviation
	 */
	public void setShotTypeAbbreviation(String shotTypeAbbreviation) {
		this.shotTypeAbbreviation = shotTypeAbbreviation;
	}
	
	/**
	 * Gets the photo srcs.
	 *
	 * @return the photo srcs
	 */
	public List<String> getPhotoSrcs() {
		return photoSrcs;
	}
	
	/**
	 * Sets the photo srcs.
	 *
	 * @param photoSrcs the new photo srcs
	 */
	public void setPhotoSrcs(List<String> photoSrcs) {
		this.photoSrcs = photoSrcs;
	}
	
	/**
	 * Gets the source.
	 *
	 * @return the source
	 */
	public String getSource() {
		return source;
	}
	
	/**
	 * Sets the source.
	 *
	 * @param source the new source
	 */
	public void setSource(String source) {
		this.source = source;
	}
	
	/**
	 * Gets the vdp order.
	 *
	 * @return the vdp order
	 */
	public String getVdpOrder() {
		return vdpOrder;
	}
	
	/**
	 * Sets the vdp order.
	 *
	 * @param vdpOrder the new vdp order
	 */
	public void setVdpOrder(String vdpOrder) {
		this.vdpOrder = vdpOrder;
	}
	
}
